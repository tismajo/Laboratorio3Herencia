public class Pantalones {
    int talla;
    String tipo;

    public Pantalones(int talla, String tipo) {
        this.talla = talla;
        this.tipo = tipo;
    }

    public int getTalla() {
        return talla;
    }

    public void setTalla(int talla) {
        this.talla = talla;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
}
